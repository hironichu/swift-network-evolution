/// A protocol that lets a protocol implementation define its own set of errors.
///
/// It consists of a domain, a code, an optional category, and a description.
///
/// - Domain should be unique.
/// - Category is usually a computed property based on the code, it enables protocol-agnostic
///   error handling. For example, `EBUSY`, HTTP/2 `ENHANCE_YOUR_CALM`, and `H3_EXCESSIVE_LOAD` are
///   all considered `excessiveLoad`.
///
/// See `NetworkPOSIXError` and `HTTP2Error` as two examples of `NetworkDomainSpecificError`.
@_spi(ProtocolProvider)
@available(Network 0.1.0, *)
public protocol NetworkDomainSpecificError: Error, Sendable, CustomStringConvertible {
    static var domain: NetworkError.Domain { get }
    var code: Int64 { get }
    static func category(of error: Self) -> NetworkError.CommonCategory?
}

/// A type-erased container for all errors generated by network protocol implementations.
///
/// It consists of a description, and at least one of a domain specific error, or a common
/// category.
///
/// A concrete error is initialized from a domain specific error. It contains a specific error
/// domain and code, as well as an optional category.
///
/// An abstract error is initialized from a category. It is useful when an app wants to
/// emit an error that protocols can translate into different error codes — for example,
/// HTTP/2 `ENHANCE_YOUR_CALM` versus `H3_EXCESSIVE_LOAD`.
@_spi(ProtocolProvider)
@available(Network 0.1.0, *)
public struct NetworkError: Error, Sendable, Hashable, CustomStringConvertible {
    private let domain: Domain?
    private let code: Int64?
    private let categoryIdentifier: String?

    public var domainSpecificError: (domain: Domain, code: Int64)? {
        if let domain, let code { (domain, code) } else { nil }
    }
    public var category: CommonCategory? {
        if let categoryIdentifier { .init(identifier: categoryIdentifier, description: description) } else { nil }
    }
    public let description: String

    public struct Domain: Sendable, Hashable, RawRepresentable {
        public let rawValue: String

        public init(rawValue: String) {
            self.rawValue = rawValue
        }
    }

    /// The identifier of a common category must be unique.
    ///
    /// If the category is specific to an area, add a nested enum and use the format `area.name`
    /// for the identifier to avoid conflicts.
    /// See `HTTP.http1FallbackRequested`.
    public struct CommonCategory: Sendable, Hashable {
        public let identifier: String
        public let description: String

        public init(identifier: String, description: String) {
            self.identifier = identifier
            self.description = description
        }

        public static var specViolation: Self { .init(identifier: "specViolation", description: "Spec Violation") }
        public static var applicationCancellation: Self {
            .init(identifier: "applicationCancellation", description: "Application Cancellation")
        }
        public static var excessiveLoad: Self { .init(identifier: "excessiveLoad", description: "Excessive Load") }
        public static var retryNeeded: Self { .init(identifier: "retryNeeded", description: "Retry Needed") }

        public static func == (lhs: Self, rhs: Self) -> Bool {
            lhs.identifier == rhs.identifier
            // Skip description
        }

        public func hash(into hasher: inout Hasher) {
            hasher.combine(identifier)
            // Skip description
        }
    }

    public init<E: NetworkDomainSpecificError>(_ error: E) {
        self.domain = E.domain
        self.code = error.code
        self.categoryIdentifier = E.category(of: error)?.identifier
        self.description = error.description
    }

    public init(category: CommonCategory) {
        self.domain = nil
        self.code = nil
        self.categoryIdentifier = category.identifier
        self.description = category.description
    }

    public static func == (lhs: Self, rhs: Self) -> Bool {
        lhs.domain == rhs.domain && lhs.code == rhs.code && lhs.categoryIdentifier == rhs.categoryIdentifier
        // Skip description
    }

    public func hash(into hasher: inout Hasher) {
        hasher.combine(domain)
        hasher.combine(code)
        hasher.combine(categoryIdentifier)
        // Skip description
    }
}

