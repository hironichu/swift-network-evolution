extension NetworkError: Codable {}
extension NetworkError.Domain: Codable {}
extension NetworkError.CommonCategory: Codable {}
#endif

@_spi(ProtocolProvider)
@available(Network 0.1.0, *)
public struct NetworkPOSIXError: NetworkDomainSpecificError {
    public static var domain: NetworkError.Domain { .init(rawValue: "POSIX") }

    public var code: Int64 {
        Int64(errorCode)
    }

    public static func category(of error: Self) -> NetworkError.CommonCategory? {
        switch error.errorCode {
        case EPROTO: .specViolation
        case ECANCELED: .applicationCancellation
        case EBUSY: .excessiveLoad
        case EAGAIN: .retryNeeded
        default: nil
        }
    }

    private let errorCode: Int32

    public init(errorCode: Int32) {
        self.errorCode = errorCode
    }

    public var description: String {
        #if !NETWORK_EMBEDDED
        let tmp: UnsafeMutablePointer<CChar>? = strerror(errorCode)
        return String(cString: tmp!)
        #else
        return "POSIX error"
        #endif
    }
}

extension NetworkError {
    public static func posix(_ errorCode: Int32) -> Self {
        .init(NetworkPOSIXError(errorCode: errorCode))
    }
}

extension NetworkError.CommonCategory {
    enum HTTP {
        public static var http1FallbackRequested: NetworkError.CommonCategory {
            .init(identifier: "HTTP.http1FallbackRequested", description: "HTTP/1 Fallback Requested")
        }
    }
}

enum HTTP2Error: Int64, NetworkDomainSpecificError {
    static var domain: NetworkError.Domain { .init(rawValue: "HTTP2") }

    case noError = 0x00
    case protocolError = 0x01
    case internalError = 0x02
    case flowControlError = 0x03
    case settingsTimeout = 0x04
    case streamClosed = 0x05
    case frameSizeError = 0x06
    case refusedStream = 0x07
    case cancel = 0x08
    case compressionError = 0x09
    case connectError = 0x0a
    case enhanceYourCalm = 0x0b
    case inadequateSecurity = 0x0c
    case http11Required = 0x0d

    var code: Int64 {
        rawValue
    }

    static func category(of error: Self) -> NetworkError.CommonCategory? {
        switch error {
        case .protocolError, .flowControlError, .streamClosed, .frameSizeError, .compressionError: .specViolation
        case .refusedStream: .retryNeeded
        case .cancel: .applicationCancellation
        case .enhanceYourCalm: .excessiveLoad
        case .http11Required: .HTTP.http1FallbackRequested
        default: nil
        }
    }

    var description: String {
        switch self {
        case .noError: return "Graceful shutdown"
        case .protocolError: return "Protocol error detected"
        case .internalError: return "Implementation fault"
        case .flowControlError: return "Flow-control limits exceeded"
        case .settingsTimeout: return "Settings not acknowledged"
        case .streamClosed: return "Frame received for closed stream"
        case .frameSizeError: return "Frame size incorrect"
        case .refusedStream: return "Stream not processed"
        case .cancel: return "Stream cancelled"
        case .compressionError: return "Compression state not updated"
        case .connectError: return "TCP connection error for CONNECT method"
        case .enhanceYourCalm: return "Processing capacity exceeded"
        case .inadequateSecurity: return "Negotiated TLS parameters not acceptable"
        case .http11Required: return "Use HTTP/1.1 for the request"
        }
    }

    static func code(for error: NetworkError) -> Int64 {
        if let domainSpecificError = error.domainSpecificError,
            domainSpecificError.domain == Self.domain
        {
            return domainSpecificError.code
        }
        if let category = error.category {
            switch category {
            case .specViolation: return Self.protocolError.rawValue
            case .applicationCancellation: return Self.cancel.rawValue
            case .excessiveLoad: return Self.enhanceYourCalm.rawValue
            case .retryNeeded: return Self.refusedStream.rawValue
            case .HTTP.http1FallbackRequested: return Self.http11Required.rawValue
            default: break
            }
        }
        return Self.internalError.rawValue
    }
}

enum TLSNetworkError: Int64, NetworkDomainSpecificError {
    static var domain: NetworkError.Domain { .init(rawValue: "tls") }

    case noError = 0x00
    case tlsError = 0x01
    case internalError = 0x03
    case handshakeFailed = 0x04
    case invalidTransportParameters = 0x05

    var code: Int64 {
        rawValue
    }

    static func category(of error: Self) -> NetworkError.CommonCategory? {
        nil
    }

    var description: String {
        switch self {
        case .noError: return "Graceful shutdown"
        case .tlsError: return "TLS error detected"
        case .internalError: return "Implementation fault"
        case .handshakeFailed: return "TLS handshake failed"
        case .invalidTransportParameters: return "Invalid transport parameters"
        }
    }

    static func code(for error: NetworkError) -> Int64 {
        if let domainSpecificError = error.domainSpecificError,
            domainSpecificError.domain == Self.domain
        {
            return domainSpecificError.code
        }
        return Self.internalError.rawValue
    }
}

extension NetworkError {
    static func tls(_ tlsError: TLSNetworkError) -> NetworkError {
        .init(tlsError)
    }
}
