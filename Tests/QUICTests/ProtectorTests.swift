@available(Network 0.1.0, *)
final class ProtectorTests: XCTestCase {
    // RFC 9001 Appendix A Sample Packet Protection
    let initialCID = QUICConnectionID([0x83, 0x94, 0xc8, 0xf0, 0x3e, 0x51, 0x57, 0x08])!

    let pnOne = PacketNumber(1)
    let pnTwo = PacketNumber(2)

    private func validateFramerKey(keys: borrowing SecFramerKeys, keyToValidate: SymmetricKey) {
        XCTAssertEqual(keys.key, keyToValidate)
    }

    private func validateFramerIVKey(keys: borrowing SecFramerKeys, keyToValidate: ProtectorIV) {
        XCTAssertTrue(keys.iv.equalTo(keyToValidate))
    }

    private func validateFramerHeaderProtectionKey(
        keys: borrowing SecFramerKeys,
        keyToValidate: SymmetricKey
    ) {
        XCTAssertEqual(keys.headerProtectionKey, keyToValidate)
    }

    func testInitialSecrets() {
        let protector = Protector(
            isClient: true,
            destinationCID: initialCID,
            logPrefixer: protectorTestsLogPrefixer
        )

        // RFC 9001 Appendix A.1 Keys, although client and server are swapped
        let clientKey = SymmetricKey(data: [
            0xcf, 0x3a, 0x53, 0x31, 0x65, 0x3c, 0x36, 0x4c, 0x88, 0xf0, 0xf3, 0x79, 0xb6, 0x06,
            0x7e, 0x37,
        ])
        let clientIV: ProtectorIV = [
            0x0a, 0xc1, 0x49, 0x3c, 0xa1, 0x90, 0x58, 0x53, 0xb0, 0xbb, 0xa0, 0x3e,
        ]
        let clientHeaderProtectionKey = SymmetricKey(data: [
            0xc2, 0x06, 0xb8, 0xd9, 0xb9, 0xf0, 0xf3, 0x76, 0x44, 0x43, 0x0b, 0x49, 0x0e, 0xea,
            0xa3, 0x14,
        ])

        validateFramerKey(
            keys: protector.readFramer[PacketKeyState.initial.rawValue],
            keyToValidate: clientKey
        )
        validateFramerIVKey(
            keys: protector.readFramer[PacketKeyState.initial.rawValue],
            keyToValidate: clientIV
        )
        validateFramerHeaderProtectionKey(
            keys: protector.readFramer[PacketKeyState.initial.rawValue],
            keyToValidate: clientHeaderProtectionKey
        )

        let serverKey = SymmetricKey(data: [
            0x1f, 0x36, 0x96, 0x13, 0xdd, 0x76, 0xd5, 0x46, 0x77, 0x30, 0xef, 0xcb, 0xe3, 0xb1,
            0xa2, 0x2d,
        ])
        let serverIV: ProtectorIV = [
            0xfa, 0x04, 0x4b, 0x2f, 0x42, 0xa3, 0xfd, 0x3b, 0x46, 0xfb, 0x25, 0x5c,
        ]
        let serverHeaderProtectionKey = SymmetricKey(data: [
            0x9f, 0x50, 0x44, 0x9e, 0x04, 0xa0, 0xe8, 0x10, 0x28, 0x3a, 0x1e, 0x99, 0x33, 0xad,
            0xed, 0xd2,
        ])

        validateFramerKey(
            keys: protector.writeFramer[PacketKeyState.initial.rawValue],
            keyToValidate: serverKey
        )
        validateFramerIVKey(
            keys: protector.writeFramer[PacketKeyState.initial.rawValue],
            keyToValidate: serverIV
        )
        validateFramerHeaderProtectionKey(
            keys: protector.writeFramer[PacketKeyState.initial.rawValue],
            keyToValidate: serverHeaderProtectionKey
        )
    }

