    func testRetryProtectionOpen() throws {
        // RFC 9001 Appendix A.4 Retry
        let retryPacket: [UInt8] = [
            0xff,  // First byte
            0x00, 0x00, 0x00, 0x01,  // Version
            0x00,  // DCIL
            0x08,  // SCIL
            0xf0, 0x67, 0xa5, 0x50, 0x2a, 0x42, 0x62, 0xb5,
            0x74, 0x6f, 0x6b, 0x65, 0x6e,  // Token
            0x04, 0xa2, 0x65, 0xba, 0x2e, 0xff, 0x4d, 0x82,
            0x90, 0x58, 0xfb, 0x3f, 0x0f, 0x24, 0x96, 0xba,
        ]
        let originalDCID = QUICConnectionID([0x83, 0x94, 0xc8, 0xf0, 0x3e, 0x51, 0x57, 0x08])!
        let destinationCID = QUICConnectionID(0)
        let sourceCID = QUICConnectionID(Array(retryPacket[7..<15]))!

        let retryPseudo = Packet.assemblePseudoRetry(
            firstByte: retryPacket[0],
            originalDCID: originalDCID,
            destinationCID: destinationCID,
            sourceCID: sourceCID,
            token: Array(retryPacket[15..<20])
        )
        XCTAssertNoThrow(
            try Protector.openRetry(
                retryPseudo: retryPseudo.span.bytes,
                retryTag: retryPacket[20..<36].span.bytes
            )
        )
    }

