@available(Network 0.1.0, *)
extension QUICStreamID {
    static func create(
        base: UInt64,
        sendEnabled: Bool,
        receiveEnabled: Bool,
        isInbound: Bool
    ) -> QUICStreamID {
        XCTAssertTrue(!sendEnabled && !receiveEnabled)  // must enable one direction
        var computedID = base

        if receiveEnabled && sendEnabled {
            // bidir
            QUICStreamID.setStreamType(value: &computedID, isBidir: true)
        } else {
            QUICStreamID.setStreamType(value: &computedID, isBidir: false)
        }

        if isInbound {
            QUICStreamID.setInitiator(value: &computedID, isLocal: false)
        } else {
            QUICStreamID.setInitiator(value: &computedID, isLocal: true)
        }
        let streamID = QUICStreamID(computedID)!
        return streamID
    }

    // Inbound. remote peer cannot create a unidirectional send only flow
    static func createInboundBidir(base: UInt64) -> QUICStreamID {
        let streamID = create(base: base, sendEnabled: true, receiveEnabled: true, isInbound: true)
        XCTAssertTrue(streamID.isBidirectional)
        return streamID
    }
    static func createInboundReceiveOnly(base: UInt64) -> QUICStreamID {
        let streamID = create(base: base, sendEnabled: false, receiveEnabled: true, isInbound: true)
        XCTAssertTrue(streamID.isReceiveOnly(server: false))
        return streamID
    }

    // Outbound. local host cannot create a unidirectional receive flow
    static func createOutboundSendOnly(base: UInt64) -> QUICStreamID {
        let streamID = create(
            base: base,
            sendEnabled: true,
            receiveEnabled: false,
            isInbound: false
        )
        XCTAssertTrue(streamID.isSendOnly(server: false))
        return streamID
    }
    static func createOutboundBidir(base: UInt64) -> QUICStreamID {
        let streamID = create(base: base, sendEnabled: true, receiveEnabled: true, isInbound: false)
        XCTAssertTrue(streamID.isBidirectional)
        return streamID
    }
}

